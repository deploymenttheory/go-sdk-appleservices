// Package fileasm is the single choke-point for generated-file scaffolding:
// the DO-NOT-EDIT header, package clause, grouped imports and gofmt.
package fileasm

import (
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

// Header is the generated-file marker. It doubles as the prune sentinel:
// files that begin with it and are not rewritten in a run are deleted.
const Header = "// Code generated by go-api-sdk-apple-dm-codegen. DO NOT EDIT.\n"

// Import is one import spec.
type Import struct {
	Alias string
	Path  string
}

// Assemble builds a complete, gofmt-formatted Go file.
func Assemble(pkg string, docComment string, imports []Import, body string) ([]byte, error) {
	var b strings.Builder
	b.WriteString(Header)
	b.WriteString("\n")
	if docComment != "" {
		b.WriteString(strings.TrimRight(docComment, "\n"))
		b.WriteString("\n")
	}
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	writeImports(&b, imports)
	b.WriteString(body)

	src, err := format.Source([]byte(b.String()))
	if err != nil {
		return nil, fmt.Errorf("generated %s file does not parse: %w", pkg, err)
	}
	return src, nil
}

// WriteFile assembles and writes a generated file, creating the directory.
func WriteFile(path, pkg, docComment string, imports []Import, body string) error {
	src, err := Assemble(pkg, docComment, imports, body)
	if err != nil {
		return fmt.Errorf("%s: %w", path, err)
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, src, 0o644)
}

func writeImports(b *strings.Builder, imports []Import) {
	if len(imports) == 0 {
		return
	}
	var std, mod []Import
	for _, imp := range imports {
		if strings.Contains(imp.Path, ".") {
			mod = append(mod, imp)
		} else {
			std = append(std, imp)
		}
	}
	sortImports(std)
	sortImports(mod)

	b.WriteString("import (\n")
	writeGroup(b, std)
	if len(std) > 0 && len(mod) > 0 {
		b.WriteString("\n")
	}
	writeGroup(b, mod)
	b.WriteString(")\n\n")
}

func sortImports(imps []Import) {
	sort.Slice(imps, func(i, j int) bool { return imps[i].Path < imps[j].Path })
}

func writeGroup(b *strings.Builder, imps []Import) {
	for _, imp := range imps {
		last := imp.Path
		if i := strings.LastIndexByte(last, '/'); i >= 0 {
			last = last[i+1:]
		}
		if imp.Alias != "" && imp.Alias != last {
			fmt.Fprintf(b, "\t%s %q\n", imp.Alias, imp.Path)
		} else {
			fmt.Fprintf(b, "\t%q\n", imp.Path)
		}
	}
}
